d = int(input("Введіть день тиждня: "))
if d == 1:
    print("Понеділок")
elif d == 2:
    print ("Вівторок")
elif d == 3:
    print ("Середа")
elif d == 4:
    print ("Четвер")
elif d == 5:
    print ("П'ятниця")
elif d == 6:
    print ("Субота")
elif d == 7:
    print ("Неділя")
else:
    print ("Невірно введений день тиждня")

